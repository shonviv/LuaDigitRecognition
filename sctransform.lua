function subtractArrays(array1, array2)
	local new = {}
	
	for i, v in pairs(array1) do
		table.insert(new, v - array2[i])
	end
	
	return new
end

function divideArrays(array1, array2)
	local new = {}
	
	for i, v in pairs(array1) do
		table.insert(new, v / array2[i])
	end
	
	return new
end

